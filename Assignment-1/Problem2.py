num1 = int(input("Enter first number: "))                   # Take two numbers from the user
num2 = int(input("Enter second number: "))

sum = num1 + num2                                           # Addition of two numbers
sub = num1 - num2                                           # Subtraction of two numbers
prod = num1 * num2                                          # Multiplication of two numbers
div = num1/num2                                             # Division of two numbers

print("\nAddition of two numbers: {}".format(sum))          # Print the output of all the four arithmetic operations
print("Subtraction of two numbers: {}".format(sub))
print("Multiplication of two numbers: {}".format(prod))
print("Division of two numbers: {}".format(div))